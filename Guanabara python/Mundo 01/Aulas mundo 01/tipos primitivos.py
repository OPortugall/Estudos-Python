valor1 = int( input('Primeiro número:'))
valor2 = int( input('Segundo número:'))

soma = valor1 + valor2

print('A soma é', soma)

"""
int == número interio (ex: 7, 102, -4)
float == números real (ex: 4.5, 0.076, 7.0)
bool == True ou False
str == 'olá', '7.5', ''
"""

print('A soma entre {} e {} vale {}'.format(valor1, valor2, soma))